// Function to apply translations
function applyLanguage(lang) {
    document.documentElement.lang = lang;
    
    // Update all elements with data-translate attribute
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update language toggle display
    const langActive = document.querySelector('.lang-active');
    const langOption = document.querySelector('.lang-option');
    
    if (langActive && langOption) {
        if (lang === 'en') {
            langActive.textContent = 'EN';
            langOption.textContent = 'ID';
        } else {
            langActive.textContent = 'ID';
            langOption.textContent = 'EN';
        }
    }
    
    // Save preference
    localStorage.setItem('jaecoo-lang', lang);
}

// Initialize language system
function initLanguageSystem() {
    const savedLang = localStorage.getItem('jaecoo-lang') || 'en';
    applyLanguage(savedLang);
    
    // Set up language toggle
    const langContainer = document.querySelector('.nav-language');
    if (langContainer) {
        langContainer.addEventListener('click', function(e) {
            const target = e.target;
            if (target.classList.contains('lang-option') || target.classList.contains('lang-active')) {
                const currentLang = document.documentElement.lang || 'en';
                const newLang = currentLang === 'en' ? 'id' : 'en';
                applyLanguage(newLang);
            }
        });
    }
}

// Global access
window.LanguageSystem = {
    applyLanguage,
    initLanguageSystem
};

// Auto-initialize
document.addEventListener('DOMContentLoaded', function() {
    initLanguageSystem();
});
